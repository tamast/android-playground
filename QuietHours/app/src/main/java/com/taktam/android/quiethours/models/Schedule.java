package com.taktam.android.quiethours.models;

import com.orm.SugarRecord;

public class Schedule extends SugarRecord<Schedule> {

    private int startHour;
    private int startMinute;
    private int endHour;
    private int endMinute;
    private boolean enabled;

    public Schedule() {

    }

    public Schedule(final int startHour, final int startMinute, final int endHour, final int endMinute, final boolean enabled) {
        this.startHour = startHour;
        this.startMinute = startMinute;
        this.endHour = endHour;
        this.endMinute = endMinute;
        this.enabled = enabled;
    }

    public int getStartHour() {
        return startHour;
    }

    public void setStartHour(int startHour) {
        this.startHour = startHour;
    }

    public int getStartMinute() {
        return startMinute;
    }

    public void setStartMinute(int startMinute) {
        this.startMinute = startMinute;
    }

    public int getEndHour() {
        return endHour;
    }

    public void setEndHour(int endHour) {
        this.endHour = endHour;
    }

    public int getEndMinute() {
        return endMinute;
    }

    public void setEndMinute(int endMinute) {
        this.endMinute = endMinute;
    }

    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }

    @Override
    public String toString() {
        return "Schedule{" +
                "startHour=" + startHour +
                ", startMinute=" + startMinute +
                ", endHour=" + endHour +
                ", endMinute=" + endMinute +
                ", enabled=" + enabled +
                '}';
    }
}
